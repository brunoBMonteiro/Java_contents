package estruturascondicionais;

public class CondicionalElseIf {
    // Condicional Else If
    public static void main(String[] args) {
        int x = 5;
        System.out.println("Bom dia");

        if (x < 0) {
            System.out.println("Boa tarde");
        } else {
            System.out.println("Boa noite");
        }
    }
}
